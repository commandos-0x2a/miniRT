float ft_det_mat4(t_mat4 *m)
{
	float det;

	det = m->m[0][0] * (m->m[1][1] * (m->m[2][2] * m->m[3][3] - m->m[2][3] * m->m[3][2])
					  - m->m[1][2] * (m->m[2][1] * m->m[3][3] - m->m[2][3] * m->m[3][1])
					  + m->m[1][3] * (m->m[2][1] * m->m[3][2] - m->m[2][2] * m->m[3][1]))
		- m->m[0][1] * (m->m[1][0] * (m->m[2][2] * m->m[3][3] - m->m[2][3] * m->m[3][2])
					- m->m[1][2] * (m->m[2][0] * m->m[3][3] - m->m[2][3] * m->m[3][0])
					+ m->m[1][3] * (m->m[2][0] * m->m[3][2] - m->m[2][2] * m->m[3][0]))
		+ m->m[0][2] * (m->m[1][0] * (m->m[2][1] * m->m[3][3] - m->m[2][3] * m->m[3][1])
					- m->m[1][1] * (m->m[2][0] * m->m[3][3] - m->m[2][3] * m->m[3][0])
					+ m->m[1][3] * (m->m[2][0] * m->m[3][1] - m->m[2][1] * m->m[3][0]))
		- m->m[0][3] * (m->m[1][0] * (m->m[2][1] * m->m[3][2] - m->m[2][2] * m->m[3][1])
					- m->m[1][1] * (m->m[2][0] * m->m[3][2] - m->m[2][2] * m->m[3][0])
					+ m->m[1][2] * (m->m[2][0] * m->m[3][1] - m->m[2][1] * m->m[3][0]));
	return (det);
}


/*
  A_{ij} is the adjugate element of the adjugate matrix
  A_{ij} = (-1)^{ij} * det(M_{ij}) 
 */
void ft_adjugate_mat4(t_mat4 *adj, t_mat4 *m)
{
	adj->m[0][0] = m->m[1][1] * m->m[2][2] * m->m[3][3] + m->m[1][2] * m->m[2][3] * m->m[3][1] + m->m[1][3] * m->m[2][1] * m->m[3][2]
		- m->m[1][1] * m->m[2][3] * m->m[3][2] - m->m[1][2] * m->m[2][1] * m->m[3][3] - m->m[1][3] * m->m[2][2] * m->m[3][1];
	adj->m[0][1] = - m->m[1][0] * m->m[2][2] * m->m[3][3] - m->m[1][2] * m->m[2][3] * m->m[3][0] - m->m[1][3] * m->m[2][0] * m->m[3][1]
		+ m->m[1][0] * m->m[2][3] * m->m[3][2] + m->m[1][2] * m->m[2][0] * m->m[3][3] + m->m[1][3] * m->m[2][2] * m->m[3][0];
	adj->m[0][2] = m->m[1][0] * m->m[2][1] * m->m[2][3] + m->m[1][1] * m->m[2][3] * m->m[3][0] + m->m[1][3] * m->m[2][0] * m->m[3][1]
		- m->m[1][3] * m->m[2][1] * m->m[3][0] - m->m[1][1] * m->m[2][0] * m->m[3][3] - m->m[1][0] * m->m[2][3] * m->m[3][1];
	adj->m[0][3] = - m->m[1][0] * m->m[2][1] * m->m[3][2] - m->m[1][1] * m->m[2][2] * m->m[3][0] - m->m[1][2] * m->m[2][0] * m->m[3][1]
		+ m->m[1][2] * m->m[2][1] * m->m[3][0] + m->m[1][1] * m->m[2][0] * m->m[3][2] + m->m[1][0] * m->m[2][2] * m->m[3][1];
	
	adj->m[1][0] = - m->m[0][1] * m->m[2][2] * m->m[3][3] - m->m[0][2] * m->m[2][3] * m->m[3][1] - m->m[0][3] * m->m[2][1] * m->m[3][2]
		+ m->m[0][3] * m->m[2][2] * m->m[3][1] + m->m[0][2] * m->m[2][1] * m->m[3][3] + m->m[0][1] * m->m[2][3] * m->m[3][2];
	adj->m[1][1] = m->m[0][0] * m->m[2][2] * m->m[3][3] + m->m[0][2] * m->m[2][3] * m->m[3][0] + m->m[0][3] * m->m[2][0] * m->m[3][2]
		- m->m[0][3] * m->m[2][2] * m->m[3][0] - m->m[0][2] * m->m[2][0] * m->m[3][3] - m->m[1][0] * m->m[2][2] * m->m[3][1];
	adj->m[1][2] = - m->m[0][0] * m->m[2][1] * m->m[2][3] - m->m[0][1] * m->m[2][3] * m->m[3][0] - m->m[0][3] * m->m[2][0] * m->m[3][1]
		+ m->m[0][3] * m->m[2][1] * m->m[3][0] + m->m[0][1] * m->m[2][0] * m->m[3][3] + m->m[0][0] * m->m[2][3] * m->m[3][1];
	adj->m[1][3] = m->m[0][0] * m->m[2][1] * m->m[3][2] + m->m[0][1] * m->m[2][2] * m->m[3][0] + m->m[0][2] * m->m[2][0] * m->m[3][1]
		- m->m[0][2] * m->m[2][1] * m->m[3][0] - m->m[0][1] * m->m[2][0] * m->m[3][2] - m->m[0][0] * m->m[2][2] * m->m[3][1];
	
	adj->m[2][0] = m->m[0][1] * m->m[1][2] * m->m[3][3] + m->m[0][2] * m->m[1][3] * m->m[3][1] + m->m[0][3] * m->m[1][1] * m->m[3][2]
		- m->m[0][3] * m->m[1][2] * m->m[3][1] - m->m[0][2] * m->m[1][1] * m->m[3][3] - m->m[0][1] * m->m[3][2] * m->m[1][3];
	adj->m[2][1] = - m->m[0][0] * m->m[1][2] * m->m[3][3] - m->m[0][2] * m->m[1][3] * m->m[3][0] - m->m[0][3] * m->m[1][0] * m->m[3][2]
		+ m->m[0][3] * m->m[1][2] * m->m[3][0] + m->m[0][2] * m->m[1][0] * m->m[3][3] + m->m[0][0] * m->m[1][3] * m->m[3][2];
	adj->m[2][2] = m->m[0][0] * m->m[1][1] * m->m[3][3] + m->m[0][1] * m->m[1][3] * m->m[3][0] + m->m[0][3] * m->m[2][0] * m->m[3][1]
		- m->m[0][3] * m->m[1][1] * m->m[3][0] - m->m[0][1] * m->m[2][0] * m->m[3][3] - m->m[0][0] * m->m[1][3] * m->m[3][1];
	adj->m[2][3] = - m->m[0][0] * m->m[1][1] * m->m[3][2] - m->m[0][1] * m->m[1][2] * m->m[3][0] - m->m[0][2] * m->m[2][0] * m->m[3][1]
		+ m->m[0][2] * m->m[1][1] * m->m[3][0] + m->m[0][1] * m->m[1][0] * m->m[3][2] + m->m[0][0] * m->m[1][2] * m->m[3][1];
	
	adj->m[3][0] = - m->m[0][1] * m->m[1][2] * m->m[2][3] - m->m[0][2] * m->m[1][3] * m->m[2][1] - m->m[0][3] * m->m[1][1] * m->m[2][2]
		+ m->m[0][3] * m->m[1][2] * m->m[2][1] + m->m[0][2] * m->m[1][1] * m->m[2][3] + m->m[0][1] * m->m[1][3] * m->m[2][2];
	adj->m[3][1] = m->m[0][0] * m->m[1][2] * m->m[2][3] + m->m[0][2] * m->m[1][3] * m->m[2][0] + m->m[0][3] * m->m[1][0] * m->m[2][2]
		- m->m[0][3] * m->m[1][2] * m->m[2][0] - m->m[0][2] * m->m[1][0] * m->m[2][3] - m->m[0][0] * m->m[1][3] * m->m[2][2];
	adj->m[3][2] = - m->m[0][0] * m->m[1][1] * m->m[2][3] - m->m[0][1] * m->m[1][3] * m->m[2][0] - m->m[0][3] * m->m[1][0] * m->m[2][1]
		+ m->m[0][3] * m->m[1][1] * m->m[2][0] + m->m[0][1] * m->m[1][0] * m->m[2][3] + m->m[0][0] * m->m[1][3] * m->m[2][1];
	adj->m[3][3] = m->m[0][0] * m->m[1][1] * m->m[2][2] + m->m[0][1] * m->m[1][2] * m->m[2][0] + m->m[0][2] * m->m[1][0] * m->m[2][1]
		- m->m[0][2] * m->m[1][1] * m->m[2][0] - m->m[0][1] * m->m[1][0] * m->m[2][2] - m->m[0][0] * m->m[1][2] * m->m[2][1];
}

void ft_inverse_mat4(t_mat4 *result, t_mat4 *m)
{
	float det;

	(void)result;
	det = ft_det_mat4(m);
	LOG_INFO("det of m = %f", det);
}
